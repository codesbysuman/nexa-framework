/**
* Nexa Template Compiler
* -----------------------
* Converts a Nexa blueprint (static VNode tree)
* into a hardcoded template function string.
*
* Supports:
* - Primitive placeholders
* - VNode placeholders
* - Array placeholders (converted to fragment)
* - Static props
* - Nested structures
*
* Does NOT mutate blueprint.
*/

export function compileNexaTemplate(blueprint, functionName = "CompiledTemplate") {
    if (!blueprint || typeof blueprint !== "object") {
        throw new Error("Invalid Nexa blueprint supplied to compiler.");
    }

    function compileNode(node) {
        if (!node) return "null";

        // 🔹 Placeholder support (primitive, vnode, array)
        if (node.isPlaceholder) {
            return `
(() => {
    const value = ctx.${node.key};
    if (value == null)
        return { tag: "text", prop: null, children: [""] };

    if (typeof value === "object" && value.tag)
        return cloneVNode(value);

    if (Array.isArray(value))
        return { tag: "fragment", prop: null, children: value };

    return { tag: "text", prop: null, children: [String(value)] };
})()
`.trim();
        }

        // 🔹 Text node
        if (node.tag === "text") {
            const textContent = node.children?.[0] ?? "";
            return `{ tag: "text", prop: null, children: [${JSON.stringify(textContent)}] }`;
        }

        // 🔹 Fragment node
        if (node.tag === "fragment") {
            const compiledChildren = (node.children || [])
                .map(child => compileNode(child))
                .join(", ");

            return `{
    tag: "fragment",
    prop: null,
    children: [${compiledChildren}]
}`;
        }

        // 🔹 Normal element
        const compiledChildren = (node.children || [])
            .map(child => compileNode(child))
            .join(", ");

        const compiledProps = node.prop
            ? JSON.stringify(node.prop, null, 2)
            : "null";

        return `{
    tag: "${node.tag}",
    prop: ${compiledProps},
    children: [${compiledChildren}]
}`;
    }

    const compiledTree = compileNode(blueprint);

    return `
/**
* 🔥 Auto-generated Nexa Compiled Template
* Function: ${functionName}
* Generated by Nexa Template Compiler
*/

export function ${functionName}(ctx = {}) {
    return ${compiledTree};
}
`.trim();
}